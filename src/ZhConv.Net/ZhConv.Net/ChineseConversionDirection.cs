namespace ZhConv.Net;

/// <summary>
/// Specifies the direction of Chinese character conversion.
/// </summary>
public enum ChineseConversionDirection
{
    /// <summary>
    /// Convert Simplified Chinese characters to Traditional Chinese characters.
    /// </summary>
    ToTraditional,

    /// <summary>
    /// Convert Traditional Chinese characters to Simplified Chinese characters.
    /// </summary>
    ToSimplified,
}